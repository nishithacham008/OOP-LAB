class ArithmeticOperations {
    public static void main(String[] args) {
        int a = 10;
        double b = 5.5;

        double sum = a + b;
        double diff = a - b;
        double prod = a * b;
        double div = a / b;

        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + diff);
        System.out.println("Product: " + prod);
        System.out.println("Division: " + div);
    }
}